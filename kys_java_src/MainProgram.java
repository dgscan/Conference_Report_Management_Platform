    private void submitArticleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitArticleActionPerformed
        // TODO add your handling code here:
        Article ar = new Article ();        
        
        /*
        System.out.println(title.getText());
        System.out.println(articleText.getText());
        System.out.println(authors.getText());
        System.out.println(contact.getText());
        System.out.println(summary.getText());
        System.out.println(keywords.getText());
        System.out.println(subject.getText());*/
        
        if(!title.getText().isEmpty() && !articleText.getText().isEmpty() && !authors.getText().isEmpty()&&
                !contact.getText().isEmpty() && !summary.getText().isEmpty() && 
                !keywords.getText().isEmpty() && !subject.getText().isEmpty()){
        
        //set
        try{
            ar.SetTitle(title.getText());
            ar.SetArticleText(articleText.getText());
            ar.SetAuthors(authors.getText());
            ar.SetContact(contact.getText());
            ar.SetSummary(summary.getText());
            ar.SetKeywords(keywords.getText());
            ar.SetSubject(subject.getText());
            
            db.OpenConnection();
            
            //send values to database class associated method if true returns clear fields
            boolean recordSuccess = db.RecordArticle(GetUserID(),ar.GetTitle(), ar.GetArticleText(), 
                ar.GetAuthors(), ar.GetContact(), ar.GetSummary(), ar.GetKeywords(), ar.GetSubject());    
            
            if(recordSuccess){
                db.CloseDB();
                //clear input fields
                title.setText("");
                articleText.setText("");
                authors.setText("");
                contact.setText("");
                summary.setText("");
                keywords.setText("");
                subject.setText("");//end of clear
            }
        }catch(Exception e){
            db.CloseDB();
            System.out.println("Error : "+ e.getMessage());
            JOptionPane.showMessageDialog(null,"Registiration is unsuccesful!");
            
        }
        }
        else{
            //fields cannot be empty
            JOptionPane.showMessageDialog(null,"Registiration is unsuccesful! Fields can not be empty!");
        }
        
        
    }//GEN-LAST:event_submitArticleActionPerformed


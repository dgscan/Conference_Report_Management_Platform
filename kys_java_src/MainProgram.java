    private void shuffleReviewersButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shuffleReviewersButtonActionPerformed
        // TODO add your handling code here:
        try{
        int length = reviewerList.getModel().getSize();
        Random random = new Random();
        reviewer1=random.nextInt(length);
        reviewer2=random.nextInt(length);
        reviewer3=random.nextInt(length);
        
        //check if randomly allocated values are equal to each
        while(reviewer1==reviewer2 || reviewer1==reviewer3){
            reviewer1=random.nextInt(length);
        }
        while(reviewer2==reviewer1 || reviewer2==reviewer3){
            reviewer2=random.nextInt(length);
        }
        //end of check
        
        indices = new int[]{reviewer1, reviewer2, reviewer3};
        reviewerList.setSelectedIndices(indices);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"You should choose an article first! "+ ex.getMessage());
        }
    }//GEN-LAST:event_shuffleReviewersButtonActionPerformed

